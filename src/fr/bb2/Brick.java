package fr.bb2;

public class Brick {

    public int posX;
    public int posY;
    public int width;
    public int height;
    public int type;

    public Brick(int x, int y, int w, int h, int t){
        posX = x;
        posY = y;
        width = w;
        height = h;
        type = t;
    }
}
